/* Enrollmentdb.SQL														*/
/* Script file for MySQL DBMS											*/
/* This script file creates the following independent tables:			*/
/* STUDENT, TEACHER, ACADEMIC_YEAR, GRADE_LEVEL, SUBJECTS		*/
/* and the following dependent tables:									*/
/* CURRICULUM, QUARTERS, SCHEDULES, SECTION, HOMEROOM, ENROLLMENT		*/
/* and loads the default data rows										*/


DROP TABLE ENROLLMENT;
DROP TABLE HOMEROOM;
DROP TABLE SECTION;
DROP TABLE SCHEDULES;
DROP TABLE QUARTERS; 
DROP TABLE CURRICULUM;

DROP TABLE STUDENT;
DROP TABLE TEACHER;
DROP TABLE ACADEMIC_YEAR;
DROP TABLE GRADE_LEVEL;
DROP TABLE SUBJECTS;
DROP DATABASE ENROLLMENTDB;

CREATE DATABASE ENROLLMENTDB;
USE ENROLLMENTDB;


CREATE TABLE STUDENT (
STUDENT_CODE INTEGER NOT NULL AUTO_INCREMENT PRIMARY KEY,
STUDENT_LRN BIGINT(12) ZEROFILL DEFAULT 0,
STUDENT_USERNAME VARCHAR(50),
STUDENT_PASSWORD VARCHAR(70),
ENROLLMENTFORM_STATUS BOOL DEFAULT FALSE,
STUDENT_STATUS BOOL DEFAULT TRUE,
GRADE_LEVEL TINYINT,
RETURNING VARCHAR(15),
WITH_LRN VARCHAR(15),
STUDENT_FNAME VARCHAR(35),
STUDENT_LNAME VARCHAR(35),
STUDENT_MNAME VARCHAR(35),
STUDENT_GENDER VARCHAR(15),
STUDENT_BIRTHDATE DATE,
STUDENT_PLACE_OF_BIRTH VARCHAR(100),
FATHER_FNAME VARCHAR(45),
FATHER_LNAME VARCHAR(45),
FATHER_MNAME VARCHAR(45),
FATHER_CONTACTNUM VARCHAR(50),
MOTHER_FNAME VARCHAR(45),
MOTHER_LNAME VARCHAR(45),
MOTHER_MNAME VARCHAR(45),
MOTHER_CONTACTNUM VARCHAR(50),
GUARDIAN_FNAME VARCHAR(45),
GUARDIAN_LNAME VARCHAR(45),
GUARDIAN_MNAME VARCHAR(45),
GUARDIAN_CONTACTNUM VARCHAR(50),
CURRENTHOUSESTREETNUM VARCHAR(50),
CURRENTSTREETNAME VARCHAR(50),
CURRENTBARANGAY VARCHAR(50),
CURRENTMUNICIPALITY VARCHAR(50),
CURRENTPROVINCE VARCHAR(50),
CURRENTCOUNTRY VARCHAR(50),
CURRENTZIPCODE VARCHAR(50),
CURRENTHOUSENUM VARCHAR(50),
PERMANENTHOUSESTREETNUM VARCHAR(50),
PERMANENTSTREETNAME VARCHAR(50),
PERMANENTBARANGAY VARCHAR(50),
PERMANENTMUNICIPALITY VARCHAR(50),
PERMANENTPROVINCE VARCHAR(50),
PERMANENTCOUNTRY VARCHAR(50),
PERMANENTZIPCODE VARCHAR(50),
PERMANENTHOUSENUM VARCHAR(50),
4PS_BENEFECIARY VARCHAR(15),
4PS_HOUSEHOLDIDNUM VARCHAR(50),
LASTGRADELEVELCOMPLETED VARCHAR(50),
LASTSCHOOLATTENDED VARCHAR(50),
LASTSCHOOLYEARCOMPLETED VARCHAR(50),
LASTSCHOOLID VARCHAR(50),
INDEX (STUDENT_CODE));

CREATE TABLE TEACHER (
TEACHER_ID INTEGER NOT NULL PRIMARY KEY,
TEACHER_FNAME VARCHAR(35),
TEACHER_LNAME VARCHAR(35),
TEACHER_MNAME VARCHAR(35),
TEACHER_CONTACTNUM VARCHAR(50),
TEACHER_EXPERTISE VARCHAR(50),
TEACHER_ADDRESS VARCHAR(50),
TEACHER_EMAIL VARCHAR(70),
TEACHER_USERNAME VARCHAR(50),
TEACHER_PASSWORD VARCHAR(70),
ACCOUNT_TYPE VARCHAR(35),
INDEX (TEACHER_ID));

CREATE TABLE ACADEMIC_YEAR (
AY_CODE INTEGER NOT NULL AUTO_INCREMENT PRIMARY KEY,
AY_YEAR VARCHAR(10),
AY_STATUS BOOL DEFAULT TRUE);

CREATE TABLE GRADE_LEVEL (
GRADELEVEL_CODE INTEGER NOT NULL AUTO_INCREMENT PRIMARY KEY,
GRADELEVEL TINYINT);

CREATE TABLE SUBJECTS (
SUBJECT_ID INTEGER NOT NULL AUTO_INCREMENT PRIMARY KEY,
SUBJECT_NAME VARCHAR(100),
SUBJECT_DESCRIPTION VARCHAR(150));

CREATE TABLE CURRICULUM (
CURR_ID INTEGER NOT NULL AUTO_INCREMENT PRIMARY KEY,
GRADELEVEL_CODE INTEGER,
CONSTRAINT FK_GRADELEVEL_CURRICULUM FOREIGN KEY(GRADELEVEL_CODE) REFERENCES GRADE_LEVEL(GRADELEVEL_CODE));

CREATE TABLE QUARTERS (
QUARTER_CODE INTEGER NOT NULL AUTO_INCREMENT PRIMARY KEY,
SUBJECT_ID INTEGER,
CURR_ID INTEGER,
QUARTER_NUMBER TINYINT,
CONSTRAINT FK_SUBJECT_QUARTERS FOREIGN KEY (SUBJECT_ID) REFERENCES SUBJECTS(SUBJECT_ID),
CONSTRAINT FK_CURRICULUM_QUARTERS FOREIGN KEY (CURR_ID) REFERENCES CURRICULUM(CURR_ID));

CREATE TABLE SCHEDULES (
SCHEDULE_ID INTEGER NOT NULL AUTO_INCREMENT PRIMARY KEY,
TEACHER_ID INTEGER,
QUARTER_CODE INTEGER,
SCHEDULE_DAY VARCHAR(14),
SCHEDULE_STARTTIME TIME,
SCHEDULE_ENDTIME TIME,
CONSTRAINT FK_QUARTERS_SCHEDULES FOREIGN KEY (QUARTER_CODE) REFERENCES QUARTERS(QUARTER_CODE),
CONSTRAINT FK_TEACHER_SCHEDULES FOREIGN KEY (TEACHER_ID) REFERENCES TEACHER(TEACHER_ID));

CREATE TABLE SECTION (
SECTION_ID INTEGER NOT NULL AUTO_INCREMENT PRIMARY KEY,
GRADELEVEL_CODE INTEGER,
SECTION_CODE VARCHAR(50),
SECTION_DESC VARCHAR(50),
CONSTRAINT FK_GRADELEVEL_SECTION FOREIGN KEY (GRADELEVEL_CODE) REFERENCES GRADE_LEVEL(GRADELEVEL_CODE));

CREATE TABLE HOMEROOM (
HOMEROOM_ID INTEGER NOT NULL AUTO_INCREMENT PRIMARY KEY,
SECTION_ID INTEGER,
TEACHER_ID INTEGER,
CONSTRAINT FK_SECTION_HOMEROOM FOREIGN KEY (SECTION_ID) REFERENCES SECTION(SECTION_ID),
CONSTRAINT FK_TEACHER_HOMEROOM FOREIGN KEY (TEACHER_ID) REFERENCES TEACHER(TEACHER_ID));

CREATE TABLE ENROLLMENT (
ENROLLMENT_ID INTEGER NOT NULL AUTO_INCREMENT PRIMARY KEY,
STUDENT_CODE INTEGER,
TEACHER_ID INTEGER,
HOMEROOM_ID INTEGER,
AY_CODE INTEGER,
ENROLL_DATE DATE,
CONSTRAINT FK_STUDENT_ENROLLMENT FOREIGN KEY (STUDENT_CODE) REFERENCES STUDENT(STUDENT_CODE),
CONSTRAINT FK_TEACHER_ENROLLMENT FOREIGN KEY (TEACHER_ID) REFERENCES TEACHER(TEACHER_ID),
CONSTRAINT FK_HOMEROOM_ENROLLMENT FOREIGN KEY (HOMEROOM_ID) REFERENCES HOMEROOM(HOMEROOM_ID),
CONSTRAINT FK_AY_ENROLLMENT FOREIGN KEY (AY_CODE) REFERENCES ACADEMIC_YEAR(AY_CODE));



